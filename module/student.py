def student_menu():
    """เมนูย่อยสำหรับจัดการข้อมูลนักเรียน (CRUD)"""
    while True:
        print("\n===== ระบบจัดการข้อมูลนักเรียน =====")
        print("1. เพิ่มข้อมูลนักเรียน")
        print("2. ดูข้อมูลนักเรียนทั้งหมด")
        print("3. แก้ไขข้อมูลนักเรียน")
        print("4. ลบข้อมูลนักเรียน")
        print("0. กลับสู่เมนูหลัก")
        
        choice = input("กรุณาเลือกเมนู: ")
        
        if choice == '1':
            # add_student()
            print(">>> เรียกฟังก์ชัน 'เพิ่มข้อมูลนักเรียน' <<<")
        elif choice == '2':
            # view_students()
            print(">>> เรียกฟังก์ชัน 'ดูข้อมูลนักเรียนทั้งหมด' <<<")
        elif choice == '3':
            # update_student()
            print(">>> เรียกฟังก์ชัน 'แก้ไขข้อมูลนักเรียน' <<<")
        elif choice == '4':
            # delete_student()
            print(">>> เรียกฟังก์ชัน 'ลบข้อมูลนักเรียน' <<<")
        elif choice == '0':
            print("ย้อนกลับสู่เมนูหลัก...")
            break
        else:
            print("ตัวเลือกไม่ถูกต้อง กรุณาลองใหม่อีกครั้ง")